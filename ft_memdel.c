#include "libft.h"

void	ft_memdel(void **ap)
{
	int		i;
	int		i2;
	char	**s;

	s = ap;
	i = 0;
	while (s[i])
	{
		i2 = 0;
		while (s[i][i2])
		{
			free(s[i][i2]);
			i2++;
		}
		free(s[i]);
		i++;
	}
	ap = NULL;
}
