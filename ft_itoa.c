#include "libft.h"

char	*ft_itoa(int n)
{
	char	*s;
	int		i;
	int		j;

	j = 0;
	i = 0;
	s = ft_strnew(13);
	if (n < 0)
	{
		s[i++] = '-';
		n = -n;
	}
	while (n > 10)
	{
		n /= 10;
		j++;
	}
	s[i++] = ('0' - n);
	while (j > 0)
	{
		n *= 10;
		s[i++] = n;
		j--;
	}
	return (s);
}
